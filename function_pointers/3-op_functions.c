#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - returns the sum
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference
 * @a: first num
 * @b: second num
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product
 * @a: first num
 * @b: second num
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the quotient
 * @a: first num
 * @b: second num
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder
 * @a: first num
 * @b: second num
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
